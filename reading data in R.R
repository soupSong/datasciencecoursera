## Reading Data There are a few principal functions reading data into R.
## read. table, read. csv, for reading tabular data & analogous functions for wiring data is write.table
## readLines, for reading lines of a text file & analogous functions for wiring data is writeLines
## source, for reading in R code files (inverse Of dump) & analogous functions for wiring data is dump
## dget, for reading in R code files (inverse of dput) & analogous functions for wiring data is dput
## load, for reading in saved workspaces & analogous functions for wiring data is save
## unserialize, for reading single R objects in binary form & analogous functions for wiring data is serialize